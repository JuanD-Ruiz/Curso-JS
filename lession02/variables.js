// Tipos primitivos
let numero = 10; // number
let cadena = "Hola"; // string
let booleano = true; // boolean
let nulo = null; // null
let indefinido; // undefined
let simbolo = Symbol("mi simbolo"); // symbol


console.log("Tipo de datos primitivos")
console.log(numero, cadena, booleano, nulo, indefinido, simbolo);

// Tipo no primitivo
let objeto = { nombre: "Juan", edad: 20 }; // object

console.log("Tipo de datos no primitivos")
console.log(objeto);